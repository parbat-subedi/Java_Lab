package javaLab;

public class qn2 {
	public static void main(String[] args){
		//boolean
		boolean b = true;
		System.out.println("boolean: " + b);

		//byte
		byte abyte = 127;

		// byte is 8 bit value
		System.out.println("byte: " + abyte);

		//char
		char charac = 'W';
		System.out.println("char: " + charac);

		//Short
		short shlen = 306;
		System.out.println("short: " + shlen);

		//integer
		int numint = 999;
		System.out.println("int: " + numint);

		//long
		long longint = 25362814;
		System.out.println("long: " + longint);

		//float
		float floatnum = 4.1416197f;
		System.out.println("float: " + floatnum);

		//double
		double value= 25.264897456;
		System.out.println("double: " + value);
		}
		
}
